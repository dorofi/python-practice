# Define the age variable
age = 18

# Check if the person is an adult
if age >= 18:
    print("Pelnoletni")  # "Adult" in Polish
# If not an adult, check if they are a teenager
elif age >= 16:
    print("Nastolatek")  # "Teenager" in Polish
# Otherwise, the person is a child
else:
    print("Dziecko")     # "Child" in Polish
